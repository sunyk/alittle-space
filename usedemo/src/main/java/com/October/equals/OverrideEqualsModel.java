package com.October.equals;

/**
 * @author sunyang
 * @date 2018/10/17 10:18
 */
public class OverrideEqualsModel {

    public String id;

    public String name;

    public OverrideEqualsModel() {
    }

    public OverrideEqualsModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        OverrideEqualsModel that = (OverrideEqualsModel) o;

        return id.equals(that.id);
    }

    @Override
    public int hashCode() {
        return id.hashCode();
    }

    @Override
    public String toString() {
        return
                "id='" + id + '\'' +
                ", name='" + name + '\'';
    }
}

